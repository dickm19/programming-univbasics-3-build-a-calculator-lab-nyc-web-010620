def addition(num1, num2)
  sum = num1 + num2
  p sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  p difference
end

def division(num1, num2)
  quotient = num1 / num2
  p quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  p product
end

def modulo(num1, num2)
  remainder = num2 % num1
  remainder = remainder - 1
  p remainder
end

def square_root(num)
  sqroot = Math.sqrt(num)
  p sqroot
end
